    <h1>Git Basic commands</h1>
    <ul>
        <li>Clone- Used to bring a repo on a remote server to your local repo</li>
        <li>Add- Track the changes made in your project</li>
        <li>Commit - Save the changes you've made to files on your local repo</li>
        <li>Push - Upload the changes you've made to the remote server e.g GitHub</li>
        <li>Pull - Download the commits you have made from the server to your local machine</li>
    </ul>
